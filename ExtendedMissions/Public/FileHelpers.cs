/// <summary>
/// Describes the type of file content generated for procedural missions.
/// </summary>
public enum FileType
{
    /// <summary>A file without a specific type.</summary>
    Unknown,
    /// <summary>A binary file.</summary>
    Binary,
    /// <summary>An image file.</summary>
    Image,
    /// <summary>A PDF document.</summary>
    Pdf,
    /// <summary>A script file.</summary>
    Script,
    /// <summary>A plain text file.</summary>
    Text
}
